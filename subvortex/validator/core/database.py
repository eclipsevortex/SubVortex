def get_field_value(value, default=None, cast_type=None):
    """
    Returned the decoded value of the field
    """
    if value is None:
        return default

    try:
        value = value.decode("utf-8") if isinstance(value, bytes) else value
        if cast_type:
            return cast_type(value)
        return value
    except Exception:
        return default
